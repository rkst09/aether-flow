function UploadZone({ files, onFiles }: { files: File[]; onFiles: (f: File[]) => void }) {
  const [isDragging, setIsDragging] = useState(false);
  const ref = useRef<HTMLInputElement>(null);
  return (
    <div className="space-y-3">
      <div
        onDragOver={e => { e.preventDefault(); setIsDragging(true); }}
        onDragLeave={() => setIsDragging(false)}
        onDrop={e => { e.preventDefault(); setIsDragging(false); onFiles([...files, ...Array.from(e.dataTransfer.files)]); }}
        onClick={() => ref.current?.click()}
        className="rounded-2xl px-8 py-12 flex flex-col items-center gap-3 cursor-pointer transition-all hover:border-[#9CA3AF] hover:bg-[#F9FAFB]"
        style={{ border: `2px dashed ${isDragging ? "#6366F1" : "#D1D5DB"}`, background: isDragging ? "#F5F3FF" : "#FAFAFA" }}
      >
        <div className="h-12 w-12 rounded-xl flex items-center justify-center" style={{ background: "#EEF2FF" }}>
          <Upload className="h-5 w-5" style={{ color: "#6366F1" }} strokeWidth={1.5} />
        </div>
        <div className="text-center space-y-1">
          <p className="text-sm font-medium" style={{ color: "#0F172A" }}>
            {isDragging ? "Drop your screens here" : "Drop screens here, or click to upload"}
          </p>
          <p className="text-xs" style={{ color: "#6B7280" }}>PNG, JPG, WebP, PDF supported · Multiple files allowed</p>
        </div>
        <input ref={ref} type="file" multiple accept="image/*,.pdf" className="hidden"
          onChange={e => e.target.files && onFiles([...files, ...Array.from(e.target.files)])} />
      </div>
      {files.length > 0 && (
        <div className="grid grid-cols-2 sm:grid-cols-3 gap-2">
          {files.map((f, i) => (
            <div key={i} className="flex items-center gap-2 rounded-xl px-3 py-2" style={{ border: "1px solid #E5E7EB", background: "#FFFFFF" }}>
              <ImageIcon className="h-3.5 w-3.5 shrink-0" style={{ color: "#9CA3AF" }} strokeWidth={1.5} />
              <span className="text-[11px] truncate flex-1" style={{ color: "#374151" }}>{f.name}</span>
              <button onClick={e => { e.stopPropagation(); onFiles(files.filter((_, j) => j !== i)); }}>
                <X className="h-3 w-3" style={{ color: "#9CA3AF" }} strokeWidth={2} />
              </button>
            </div>
          ))}
        </div>
      )}
    </div>
  );
}

function CopyItemCard({ item }: { item: RichCopyItem }) {
  const [copied, setCopied] = useState(false);
  const sev = SEV_STYLE[item.severity] ?? SEV_STYLE.Medium;
  const cfg = COPY_TYPE_CONFIG[item.copyType] ?? { label: item.copyType, Icon: FileEdit, dot: "#64748B" };

  const handleCopy = () => {
    navigator.clipboard.writeText(item.suggested);
    setCopied(true);
    setTimeout(() => setCopied(false), 1500);
  };

  return (
    <div className="rounded-xl p-4 space-y-3" style={{ border: "1px solid #F1F5F9", background: "#FAFAFA" }}>
      {/* Header row */}
      <div className="flex items-center gap-2 flex-wrap">
        <div className="h-1.5 w-1.5 rounded-full shrink-0" style={{ background: cfg.dot }} />
        <cfg.Icon className="h-3.5 w-3.5 shrink-0" style={{ color: cfg.dot }} strokeWidth={1.5} />
        <span className="text-[11px] font-semibold" style={{ color: "#374151" }}>{cfg.label}</span>
        <span className="ml-auto text-[10px] font-semibold px-2 py-0.5 rounded-full" style={{ background: sev.badge, color: sev.text }}>
          {item.severity}
        </span>
      </div>

      {/* Original → Improved */}
      <div className="space-y-2">
        <div className="flex items-start gap-2">
          <span className="text-[10px] font-semibold mt-0.5 shrink-0" style={{ color: "#9CA3AF" }}>BEFORE</span>
          <p className="text-xs leading-relaxed line-through" style={{ color: "#6B7280" }}>{item.original}</p>
        </div>
        <div className="text-[11px] leading-relaxed px-3 py-1.5 rounded-lg" style={{ background: "#FFF7ED", color: "#92400E", border: "1px solid #FDE68A" }}>
          {item.issue}
        </div>
        <div className="flex items-start gap-2">
          <span className="text-[10px] font-semibold mt-0.5 shrink-0" style={{ color: "#059669" }}>AFTER</span>
          <p className="text-xs font-medium leading-relaxed flex-1" style={{ color: "#0F172A" }}>{item.suggested}</p>
          <button onClick={handleCopy}
            className="shrink-0 flex items-center gap-1 text-[10px] px-2 py-1 rounded-lg transition-all"
            style={{ background: copied ? "#ECFDF5" : "#F1F5F9", color: copied ? "#059669" : "#6B7280", border: "1px solid #E5E7EB" }}>
            {copied ? <Check className="h-3 w-3" strokeWidth={2} /> : <CopyIcon className="h-3 w-3" strokeWidth={1.5} />}
            {copied ? "Copied" : "Copy"}
          </button>
        </div>
      </div>
    </div>
  );
}

function ScreenCopyCard({ screen, sevFilter, previewUrl }: { screen: RichScreenCopyReview; sevFilter: SevFilter; previewUrl?: string }) {
  const visible = sevFilter === "all" ? screen.items : screen.items.filter(i => i.severity.toLowerCase() === sevFilter);
  if (visible.length === 0) return null;

  const highCount = screen.items.filter(i => i.severity === "High").length;

  // Group by copy type
  const grouped = visible.reduce<Record<string, RichCopyItem[]>>((acc, item) => {
    const key = item.copyType;
    if (!acc[key]) acc[key] = [];
    acc[key].push(item);
    return acc;
  }, {});

  return (
    <div className="rounded-2xl overflow-hidden" style={{ border: "1px solid #E5E7EB" }}>
      {/* Header */}
      <div className="px-5 py-4 flex items-center gap-4 flex-wrap" style={{ borderBottom: "1px solid #E5E7EB", background: "#FAFAFA" }}>
        <div className="flex-1 min-w-0">
          <div className="flex items-center gap-2 flex-wrap">
            <h3 className="text-sm font-bold" style={{ color: "#0F172A" }}>{screen.name}</h3>
            <span className="text-[10px] px-2 py-0.5 rounded-full" style={{ background: "#F1F5F9", color: "#64748B" }}>{screen.persona}</span>
          </div>
          <p className="text-xs mt-0.5" style={{ color: "#6B7280" }}>
            {screen.items.length} copy items · {highCount} high priority
          </p>
        </div>
      </div>

      {/* Preview */}
      {previewUrl && (
        <div className="px-5 py-4" style={{ borderBottom: "1px solid #F1F5F9" }}>
          <div className="aspect-video rounded-xl overflow-hidden flex items-center justify-center"
            style={{ background: "#F8FAFC", border: "1px solid #E5E7EB" }}>
            <img src={previewUrl} alt={screen.name} className="w-full h-full object-contain" />
          </div>
        </div>
      )}

      {/* Items by type */}
      <div className="p-5 space-y-3" style={{ background: "#FFFFFF" }}>
        {Object.entries(grouped).map(([type, items]) => (
          <div key={type}>
            <p className="text-[10px] font-semibold uppercase tracking-wide mb-2" style={{ color: "#9CA3AF" }}>
              {COPY_TYPE_CONFIG[type]?.label ?? type}
            </p>
            <div className="space-y-2">
              {items.map(item => <CopyItemCard key={item.id} item={item} />)}
            </div>
          </div>
        ))}
      </div>
    </div>
  );
}

