function downloadBlob(content: string, filename: string, mime: string) {
  const blob = new Blob([content], { type: mime });
  const url = URL.createObjectURL(blob);
  const a = document.createElement("a");
  a.href = url; a.download = filename; a.click();
  URL.revokeObjectURL(url);
}

function exportExcel(screens: RichScreenCopyReview[]) {
  const header = ["Screen", "Copy Type", "Original", "Issue", "Severity", "Improved Copy"];
  const rows = screens.flatMap(s => s.items.map(i => [
    s.name,
    COPY_TYPE_CONFIG[i.copyType]?.label ?? i.copyType,
    i.original,
    i.issue,
    i.severity,
    i.suggested,
  ]));
  const csv = "\uFEFF" + [header, ...rows].map(r => r.map(v => `"${String(v).replace(/"/g, '""')}"`).join(",")).join("\r\n");
  downloadBlob(csv, "UX_Copy_Review.csv", "text/csv;charset=utf-8;");
}

